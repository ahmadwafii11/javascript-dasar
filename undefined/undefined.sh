Undefined merupakan sebuah kata kunci yang terdapat di dalam bahasa pemrograman Java Script. Jadi, apabila ingin membuat nama variable dengan undefined, maka tidak bisa.
Undefined merupakan sebuah tipe data.
Undefined itu merepresentasikan sebuah data yang belum didefinisikan.
Suatu variabe yang belum ditambahkan nilai atau value, maka ini dapat disebut sebagai variable tipe undefined.
Gambaran umumnya:
- Apabila membuat sebuah array, namun ingin mengakses suatu index yang tidak ada, maka ini disebut sebagai undefined.
- Apabila membuat sebuah object, namun ingin mengakses suatu properti yang tidak ada, maka ini disebut sebagai undefined.
Terkadang, untuk programmer Java Script pemula, undefined ini memang agak sedikit membingungkan.
Perlu catatan, bahwa undefined itu berbeda dengan null di bahasa pemrograman lain.

