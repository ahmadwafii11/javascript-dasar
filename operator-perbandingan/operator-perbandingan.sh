Operasi perbandingan merupakan operasi untuk membandingkan 2 buah data
Hasil dari operasi perbandingan ini adalah nilai boolean (benar atau salah)
- Jika hasil operasi yang didapatkan adalah benar, maka nilainya adalah true
- Jika hasil operasi yang didapatkan adalah salah, maka nilainya adlaah false

         ----------------------
        |Operator Perbandingan |
         ----------------------
--------------------------------         
operator        | keterangan
--------------------------------
>               | Lebih dari
<               | Kurang dari
>=              | Lebih dari sama dengan
<=              | Kurang dari sama dengan
==              | Sama dengan
===             | Sama dengan dan sama tipe (datanya)
!=              | Tidak sama dengan
!==             | Tidak sama dengan atau tidak sama tipe (datanya)