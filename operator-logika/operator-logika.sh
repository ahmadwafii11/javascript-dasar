Operator logika merupakan operator untuk dua buah data boolean (True or False)
Operator ini digunakan hanya untuk sebuah tipe data boolean (True or False)
Hasil dari operator logika ini adalah nilai booelan lagi

         -----------------
        |Operator Logika |
         -----------------
--------------------------------         
operator        | keterangan
--------------------------------
&&              | Dan
||              | Atau
!               | Kebalikan


         -------------------
        |Operator && (Dan) |
         -------------------

---------------------------------------------------------------------        
Nilai 1         | Operator          | Nilai 2           | Hasil
---------------------------------------------------------------------
True            | &&                | True              | True
False           | &&                | True              | False
True            | &&                | False             | False
False           | &&                | False             | False

        ---------------------
        |Operator || (Atau) |
        ---------------------

---------------------------------------------------------------------        
Nilai 1         | Operator          | Nilai 2           | Hasil
---------------------------------------------------------------------
True            | ||                | True              | True
False           | ||                | True              | True
True            | ||                | False             | True
False           | ||                | False             | False

        ---------------------
        |Operator ! (Unary) |
        ---------------------
Operator Unary merupakan sebuah operator yang digunakan untuk satu buah data saja. Terlihat seperti kebalikan saja.

---------------------------------------------------------------------        
Operator          | Nilai 2           | Hasil
---------------------------------------------------------------------
!                 | True              | False
!                 | False             | True

