Tipe data string atau text merupakan tipe data yang berisikan kumpulan kosong atau lebih karakter.
Sebelumnya, kita telah menggunakan tipe data string pada studi kasus pembuatan program hello world.
Di dalam Java Script, untuk membuat tipe data string hanya perlu menggunakan " (petik dua) atau ' (petik satu) sebelum dan setelah isi data string atau textnya.

Menambah String:
Untuk melakukan penambahan atau penggabungan dari beberapa tipe data string, cukup dengan menggunakan operator + (plus).

Escape Sequence:
Java Script mendukung escape sequence di dalam tipe data string. Escape sequence merupakan karakter khusus yang seperti ENTER, TAB, ""(kutip dua), dan lain-lain.
Berikut merupakan contoh escape sequence yang didukung oleh Java Script di dalam tipe data string.

\n  = ENTER
\t  = TAB
\'  = '
\"  = "
\\  = \