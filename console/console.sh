Console merupakan fitur yang dimiliki oleh Java Script untuk melakukan logging. 
Logging merupakan mekanisme yang biasa dilakukan oleh seorang programmer untuk menampilkan informasi dari aplikasi yang sedang berjalan, tanpa harus mengganggu alur kerja aplikasi dan juga interaksi user.
Untuk melakukan logging, kita dapat menggunakan fitur Console di Java Script.
Untuk menggunakan fitur Console, kita cukup gunakan perintah console di kode Java Script
Untuk menampilkan console, buka chrome --> klik kanan --> pilih inspect --> pilih console

            ------------------
            | Console Method |
            ------------------
----------------------------------------------
Console Method              | keterangan
----------------------------------------------
console.info(...)           | Memberitahu informasi
console.log(...)            | Memberitahu informasi
console.warn(...)           | Memberitahu informasi peringatan
console.error(...)          | Memberitahu informasi error
console.table(...)          | Memberitahu informasi dalam bentuk table