Tipe data boolean merupakan tipe data yang berisikan data kebenaran, artinya hanya terdapat dua data yakni benar dan salah (yes or no).
Benar disini direpresentasikan dengan kata kunci "True" dan Salah direpresentasikan dengan kata kunci "False".
Walaupun tipe data boolean merupakan tipe data yang sangat sederhana, namun tipe data boolean akan banyak digunakan di dalam berbagai fitur pemrograman.
